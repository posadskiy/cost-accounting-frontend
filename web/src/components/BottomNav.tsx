const tabs = [
  { href: "/dashboard", label: "Dashboard", Icon: DashboardIcon },
  { href: "/add", label: "Add", Icon: AddIcon },
  { href: "/statistics/events", label: "Statistics", Icon: StatisticsIcon },
  { href: "/statistics/chart", label: "Chart", Icon: ChartIcon },
  { href: "/settings", label: "Settings", Icon: SettingsIcon },
] as const;

export default function BottomNav() {
  const { pathname } = useLocation();

  return (
    <nav
      className="fixed bottom-0 left-0 right-0 z-50 border-t border-[var(--border)] bg-[var(--card)] pb-[env(safe-area-inset-bottom)]"
      role="navigation"
      aria-label="Main"
    >
      <ul className="flex items-center justify-around py-2">
        {tabs.map(({ href, label, Icon }) => {
          const active =
            pathname === href ||
            (href === "/settings" && pathname.startsWith("/settings")) ||
            (href !== "/settings" && pathname.startsWith(href));
          return (
            <li key={href}>
              <Link
                to={href}
                aria-label={label}
                className={`flex justify-center px-4 py-2 ${
                  active ? "text-[var(--primary)]" : "text-[var(--foreground)] opacity-80"
                }`}
              >
                <Icon className={iconClass} />
              </Link>
            </li>
          );
        })}
      </ul>
    </nav>
  );
}
